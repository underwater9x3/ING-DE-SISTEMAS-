class TRIANGULO:
    def area(self)-> None:
        b = int(input("\n Ingrese la base: "))
        h = int(input("\n Ingrese la altura: "))
        print ("\tArea: ", (b*h)/2)

def perimetro(self)->None:
    l1 = int(input("\nIngrese lado 1:"))
    l2 = int(input("\nIngrese lado 2: "))
    l3 = int(input("\nIngrese lado 3: "))
    print ("Perimetro: ", l1+l2+l3)